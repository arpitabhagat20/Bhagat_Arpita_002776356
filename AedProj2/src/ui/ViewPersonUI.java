    private void populateTable() {
    DefaultTableModel model = (DefaultTableModel) tblViewPerson.getModel();
        model.setRowCount(0);
        
        for(Person person : logs.getLogs()){
            Object[] row= new Object[7];
            row[0] = person.getPersonName();
            row[1] = person.getPersonGender();
            row[2] = person.getPersonAge();
            row[3] = person.getPersonPhoneNumber();
            row[4] = person.getPersonUserName();
            row[5] = person.getPersonPassword();
            row[6] = person.getRole();
            
            model.addRow(row);
        }
      
    }
