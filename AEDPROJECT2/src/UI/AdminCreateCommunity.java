    private void txtaddCommunityActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_txtaddCommunityActionPerformed
        // TODO add your handling code here:
        String errorMessage = "";
        Community community = new Community();

        if (cbCity.getSelectedIndex() == -1) {
            errorMessage += "Please select City! \n";
        }
        String communityErrorMessage = community.validateCommunityName(txtCommunity.getText());
        errorMessage += communityErrorMessage;

        if (community.alreadtExists(cbCity.getSelectedItem().toString(), txtCommunity.getText())) {
            errorMessage += "Community already registered with the selected City \n";
        }
        if (errorMessage.length() != 0) {
            JOptionPane.showMessageDialog(this, errorMessage, "Add City", ERROR_MESSAGE);
        } else {
            community.communityName = txtCommunity.getText();
            community.city = cbCity.getSelectedItem().toString();
            community.createdate = new Date();
            City.allCities.add(community);
            updateTable();
            JOptionPane.showMessageDialog(this, "City added successfully!", "Addition", INFORMATION_MESSAGE);
        }
    }//GEN-LAST:event_txtaddCommunityActionPerformed

