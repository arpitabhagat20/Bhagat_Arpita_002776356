    private void updateTable() {
         DefaultTableModel model = (DefaultTableModel) tblCity.getModel();
        model.setRowCount(0);
        for (int i = 0; i < City.allCities.size(); i++) {
            Object[] row = {City.allCities.get(i).city, City.allCities.get(i).communityName, City.allCities.get(i).createdate};
            model.addRow(row);
        }
    }
